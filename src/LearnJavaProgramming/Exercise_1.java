package LearnJavaProgramming;

public class Exercise_1 {
    public static void main(String[] args) {
        // Metoda System.out.println() este folosită pentru a afișa mesaje pe consola (output).
        // 'System' este o clasă predefinită în Java care conține o varietate de metode și variabile.
        // 'out' este un obiect de tip PrintStream care reprezintă fluxul standard de ieșire (consola).
        // 'println()' este o metodă a clasei PrintStream, care afișează argumentul furnizat și trece automat pe o linie nouă după fiecare apel.
        System.out.println("Informații interesante despre Java:");
        System.out.println("1. Java a fost dezvoltat de Sun Microsystems în 1995.");
        System.out.println("2. Sloganul Java este 'Write once, run anywhere'.");
        System.out.println("3. Java rulează pe peste 3 miliarde de dispozitive în întreaga lume.");
        System.out.println("4. Java este utilizat pentru dezvoltarea de aplicații enterprise, aplicații Android și sisteme distribuite.");
        System.out.println("5. Limbajul Java a fost numit după o insulă indoneziană, cunoscută pentru cafeaua sa.");
    }
}